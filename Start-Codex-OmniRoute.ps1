`$hadPreviousUserCodexHome = $hadPreviousLiteral
`$taskkillShimDir = $taskkillShimLiteral
`$previousUserPath = $previousPathLiteral
`$expectedUserPath = $expectedPathLiteral
`$hadPreviousUserPath = $hadPreviousPathLiteral
`$activationStartedAtUtc = [datetime]$activationLiteral
`$timeoutSec = $TimeoutSec
`$activatedPid = [uint32]$ActivatedPid
`$packageRoot = $packageRootLiteral
