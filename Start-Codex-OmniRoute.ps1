function Invoke-McpRuntimeProbe {
    param(
        [Parameter(Mandatory = $true)][string]$NodeExe,
        [Parameter(Mandatory = $true)][string]$ScriptRoot,
        [Parameter(Mandatory = $true)][string]$ConfigPath
    )

    $probeScript = Join-Path $ScriptRoot 'tools\mcp_probe.mjs'
    if (-not (Test-Path -LiteralPath $probeScript)) {
        Write-Warning "[omniroute] MCP probe script not found: $probeScript"
        return
    }

    $raw = & $NodeExe $probeScript --config $ConfigPath --timeout-ms 8000 --json 2>&1
    $exit = $LASTEXITCODE
    $joined = ($raw | Out-String)
    if ($exit -ne 0) {
        Write-Warning "[omniroute] MCP probe exited with code $exit`: $($joined.Trim())"
        return
    }

    try {
        $parsed = $joined | ConvertFrom-Json -ErrorAction Stop
    } catch {
        Write-Warning "[omniroute] MCP probe returned non-JSON output: $($joined.Trim())"
        return
    }

    $bad = @()
    $coreBad = @()
    $discoverableStatuses = @('tools_listed', 'callable')
    foreach ($result in @($parsed.results)) {
        if ($discoverableStatuses -contains [string]$result.status) { continue }
        $bad += ("{0}={1}" -f $result.name, $result.status)
        if ($result.name -in @('magic', 'shadcn')) {
            $coreBad += ("{0}={1}" -f $result.name, $result.status)
        }
    }
    $listedCount = @($parsed.results | Where-Object { $discoverableStatuses -contains [string]$_.status }).Count
    $callableCount = @($parsed.results | Where-Object { $_.status -eq 'callable' }).Count
    $urlCount = @($parsed.results | Where-Object { $_.transport -eq 'http' }).Count
    if ($bad.Count -eq 0) {
        Write-Host ("[omniroute] MCP runtime probe ok (discoverable={0}, callable={1}, url={2})" -f $listedCount, $callableCount, $urlCount)
    } elseif ($coreBad.Count -gt 0) {
        throw ("[omniroute] core frontend MCP probe failed: {0}" -f ($coreBad -join ', '))
    } else {
        Write-Warning ("[omniroute] MCP runtime probe found non-core failures: {0}" -f ($bad -join ', '))
    }
}

