        # Resolve relative paths against the script root (where the launcher
        # lives), not the cwd, so that double-clicking the .bat from any
        # directory still finds the provider config.
        $resolvedProvider = Join-Path $scriptRoot $ProviderJson
    }
}

# CODEX_HOME is the only Codex env override we set. We deliberately do
# NOT touch USERPROFILE / APPDATA / HOME / LOCALAPPDATA / TEMP / TMP --
# those keep pointing at the user's real profile so apply_patch, MCP,
# git, rg, file dialogs, ~/.gitconfig, ~/.ssh continue to work.
#
# CODEX_HOME is set at PROCESS scope here so the bridge child process
# inherits it. AppX activation is brokered by Windows and does not
# inherit arbitrary process-scope variables from this launcher; the Codex
# GUI launch below handles that with a short-lived user-scope override.
$bridgeEnv = @{
    CODEX_HOME              = $isolatedHome
    CODEX_BRIDGE_HOST       = '127.0.0.1'
    CODEX_BRIDGE_PORT       = "$port"
    BRIDGE_LOG_PATH         = $bridgeLog
}
if ($resolvedProvider -and (Test-Path -LiteralPath $resolvedProvider)) {
    $bridgeEnv['OMNIROUTE_PROVIDER_JSON'] = $resolvedProvider
}

# Apply env vars at process scope for the bridge. The non-CODEX_HOME
# bridge variables (BRIDGE_LOG_PATH, etc.) are also kept since they do
# not affect Codex Desktop.
foreach ($kv in $bridgeEnv.GetEnumerator()) {
    [System.Environment]::SetEnvironmentVariable($kv.Key, [string]$kv.Value, 'Process')
}

"[$(Get-Date -Format o)] bridge starting node=$nodeExe port=$port codex_home=$isolatedHome" |
    Out-File -LiteralPath $bridgeLog -Encoding UTF8 -Append

$proc = $null
# -WindowStyle is Windows-only; only pass it where supported so the
# launcher also works under pwsh on Linux/macOS in CI/verification.
$startArgs = @{
    FilePath         = $nodeExe
    ArgumentList     = @($bridgeScript)
    WorkingDirectory = $scriptRoot
    PassThru         = $true
}
if (Test-WindowsHost) {
    $startArgs['WindowStyle'] = 'Hidden'
}
$proc = Start-Process @startArgs

if (-not $proc) {
    throw "[omniroute] failed to start bridge process (Start-Process returned null)"
}
try { $proc.PriorityClass = [System.Diagnostics.ProcessPriorityClass]::BelowNormal } catch {}
Set-Content -LiteralPath $bridgePid -Value $proc.Id -Encoding ASCII

try {
    $health = Wait-ForBridgeHealth -BridgeHost '127.0.0.1' -Port $port -TimeoutSec 25
    $omniSource = if ($health -and $health.omniroute) { $health.omniroute.source } else { '<unknown>' }
    Write-Host "[omniroute] bridge healthy on 127.0.0.1:$port (pid=$($proc.Id), source=$omniSource)"
} catch {
    Write-Error "[omniroute] bridge failed to come up: $($_.Exception.Message). See $bridgeLog."
    try { Stop-Process -Id $proc.Id -Force -ErrorAction SilentlyContinue } catch {}
    throw
}

if ($NoCodex) {
    Write-Host "[omniroute] -NoCodex: bridge is running, isolated CODEX_HOME is seeded. Codex GUI not launched."
    exit 0
}

# ---- Launch Codex via AppX broker ----------------------------------------
#
# Activating through IApplicationActivationManager gives Codex.exe its
# Microsoft Store package identity, which propagates to every child
# process Codex spawns (agent shell, apply_patch.bat -> codex.exe,
# codex-command-runner, rg, etc.). This is the same launch path
# Start Menu uses.
#
# Before activation, CODEX_HOME is written to the user's Windows
# environment. The AppX broker and late Desktop helpers build packaged
# process environments from user/machine environment state, not from this
# PowerShell process's private environment block. A hidden watcher keeps
# the user-scope override leased until the activated Desktop/AppX process
# tree exits, then restores the previous user values.

$argumentString = ''
if ($OpenProject) {
    $argumentString = "--open-project `"$OpenProject`""
}

$previousUserCodexHome = $null
$previousUserPath = $null
$expectedUserPath = $null
$setUserCodexHomeForAppx = $false
$setUserPathForAppx = $false
$activationStartedAtUtc = (Get-Date).ToUniversalTime()
if (Test-WindowsHost) {
    $previousUserCodexHome = [System.Environment]::GetEnvironmentVariable('CODEX_HOME', 'User')
    if (Test-SameEnvironmentValue $previousUserCodexHome $isolatedHome) {
        # Treat an already-present value that points at our isolated home as
        # stale state from an interrupted prior launch. We can reuse it for
        # this activation, but we still clear it in the finally block below.
        $previousUserCodexHome = $null
        $setUserCodexHomeForAppx = $true
        Write-Host "[omniroute] found stale user-scope CODEX_HOME; will clear it after AppX activation"
    } else {
        Set-UserCodexHomeOverride -Value $isolatedHome
        $setUserCodexHomeForAppx = $true
        Write-Host "[omniroute] temporarily set user-scope CODEX_HOME for AppX activation"
    }

    if ($taskkillShimDir -and (Test-Path -LiteralPath (Join-Path $taskkillShimDir 'taskkill.exe'))) {
        $previousUserPath = [System.Environment]::GetEnvironmentVariable('PATH', 'User')
        $expectedUserPath = Join-PathPrefix -PathValue $previousUserPath -Prefix $taskkillShimDir
        if (-not [string]::Equals($previousUserPath, $expectedUserPath, [System.StringComparison]::OrdinalIgnoreCase)) {
            Set-UserPathOverride -Value $expectedUserPath
            $setUserPathForAppx = $true
            Write-Host "[omniroute] temporarily prepended taskkill quiet shim to user-scope PATH for AppX activation"
        }
    } else {
        Write-Warning "[omniroute] taskkill quiet shim is unavailable; MCP app-server stdio may still be polluted by taskkill output."
    }
}

$activatedPid = 0
$activationSucceeded = $false
try {
    $activatedPid = Start-CodexViaAppx -AumId $appx.AumId -Arguments $argumentString
    $activationSucceeded = $true
} finally {
    if ($setUserCodexHomeForAppx -or $setUserPathForAppx) {
        if ($activationSucceeded) {
            $watcherStarted = Start-UserCodexHomeRestoreWatcher `
                -IsolatedHome $isolatedHome `
                -PreviousUserCodexHome $previousUserCodexHome `
                -TaskkillShimDir $(if ($setUserPathForAppx) { $taskkillShimDir } else { $null }) `
                -PreviousUserPath $previousUserPath `
                -ExpectedUserPath $expectedUserPath `
                -TimeoutSec $UserCodexHomeRestoreTimeoutSec `
                -ActivationStartedAtUtc $activationStartedAtUtc `
                -ActivatedPid $activatedPid `
                -PackageInstallLocation $appx.InstallLoc
            if (-not $watcherStarted) {
                Restore-UserCodexHomeOverrideNow `
                    -IsolatedHome $isolatedHome `
                    -PreviousUserCodexHome $previousUserCodexHome `
                    -TaskkillShimDir $(if ($setUserPathForAppx) { $taskkillShimDir } else { $null }) `
                    -PreviousUserPath $previousUserPath `
                    -ExpectedUserPath $expectedUserPath
            } else {
                Write-Host "[omniroute] user-scope CODEX_HOME/PATH overrides will be restored after Codex Desktop and its AppX helpers exit"
            }
        } else {
            Restore-UserCodexHomeOverrideNow `
                -IsolatedHome $isolatedHome `
                -PreviousUserCodexHome $previousUserCodexHome `
                -TaskkillShimDir $(if ($setUserPathForAppx) { $taskkillShimDir } else { $null }) `
                -PreviousUserPath $previousUserPath `
                -ExpectedUserPath $expectedUserPath
        }
    }
}
if ($activatedPid -gt 0) {
    Write-Host "[omniroute] launched Codex via AppX activation (pid=$activatedPid)"
} else {
    Write-Host "[omniroute] Codex AppX activation succeeded (pid not reported)"
}

Write-Host "[omniroute] tail the bridge log to watch routing:"
Write-Host "  Get-Content '$bridgeLog' -Tail 50 -Wait"
