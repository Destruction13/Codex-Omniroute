        [AllowNull()][string]$PreviousUserPath,
        [AllowNull()][string]$ExpectedUserPath,
        [int]$TimeoutSec = 0,
        [Parameter(Mandatory = $true)][datetime]$ActivationStartedAtUtc,
        [uint32]$ActivatedPid = 0,
        [AllowNull()][string]$PackageInstallLocation
    )
    if (-not (Test-WindowsHost)) { return $false }
    if ($TimeoutSec -lt 0) { $TimeoutSec = 0 }

