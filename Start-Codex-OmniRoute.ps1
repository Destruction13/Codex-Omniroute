<#
.SYNOPSIS
    OmniRoute launcher (Variant 3): official Codex with main reasoning
    rerouted to OmniRoute, via a narrowly-isolated CODEX_HOME.

.DESCRIPTION
    Codex OmniRoute is intentionally a thin mode switch on top of the
    unmodified Microsoft Store Codex app. The architecture has three
    moving parts:

      1. A local Node bridge (codex-openai-omniroute-bridge.mjs) that:
           - reroutes /v1/responses and /v1/chat/completions to OmniRoute;
           - forwards everything else (compact, dictation, models cache,
             account stuff) to the official upstream unchanged.

      2. An isolated CODEX_HOME (".codex-omniroute-home" next to this
         launcher) seeded from the user's real ~/.codex:
             auth.json         <- copied from user's real auth.json
                                  (keeps the user signed in with the SAME
                                  ChatGPT account; fast mode + ChatGPT
                                  credits keep working).
             models_cache.json <- copied if present, so the bridge's
                                  /v1/models route has data.
             config.toml       <- regenerated by this launcher; selects
                                   model_provider = "omniroute_bridge"
                                   and imports selected tooling sections.
         state_5.sqlite*, logs_2.sqlite*, sessions/, session_index.jsonl,
         and .codex-global-state.json are preserved in place so OmniRoute
         chat history survives restarts. The user's real ~/.codex history
         is NEVER touched.

      3. The launcher exports CODEX_HOME=<isolated path> into its own
         process environment for the bridge child process, then writes
         the same value to the user's Windows environment before AppX
         activation. AppX activation and late Desktop helpers are
         brokered by Windows and do not inherit arbitrary process-scope
         variables from this PowerShell process. A hidden restore watcher
         restores the previous user value after the activated Codex
         Desktop/AppX process tree has exited. During the same lease, it
         also prepends a generated quiet taskkill.exe shim to user-scope
         PATH so Codex app-server MCP cleanup cannot pollute JSON-RPC
         stdout with taskkill success text; the same watcher restores the
         previous PATH. USERPROFILE / APPDATA / HOME / LOCALAPPDATA /
         TEMP / TMP are NOT overridden -- so apply_patch, MCP, git, rg,
         file dialogs, ~/.gitconfig, ~/.ssh continue to resolve against
         the user's real Windows profile.

    The launcher never writes OmniRoute managed blocks or auth sentinels
    into the user's real %USERPROFILE%\.codex directory. If the real
    config.toml is detected as the known mojibake/path-corruption case,
    it is backed up and repaired from the known-good pre-normalize backup.
    As a result there is no restore-of-managed-config to do on the way
    back out -- the official launcher just stops the bridge and helpers.

    For users upgrading from earlier versions (PR #3 + earlier) we DO
    sweep up any legacy artifacts left in the user's real ~/.codex by
    those launchers: a leftover managed block in config.toml, a
    sentinel auth.json, and the *.codex-omniroute-backup files. The
    sweep is a best-effort one-shot reverse-of-PR-#3 so the user does
    not have to manually clean their profile when switching to this
    version.

    Codex.exe itself is launched through the AppX activation broker
    (IApplicationActivationManager), exactly the way Start-Menu and
    Start-Codex-Official.ps1 launch it. This preserves Microsoft Store
    package identity for Codex AND for every child shell it spawns, so
    codex-command-runner.exe, rg.exe, and other bundled CLI tools stay
    in the AppX-launched context. Because Store-provided apply_patch.bat
    can resolve to an inaccessible WindowsApps target in child shells, a
    tiny local watcher rewrites that wrapper to the local Codex CLI helper.

.PARAMETER NoCodex
    Start the bridge and seed the isolated CODEX_HOME, but do NOT launch
    the Codex GUI. Used by verify-codex-omniroute.ps1.

.PARAMETER Restore
    Stop the managed bridge and wipe the isolated CODEX_HOME directory.
    Also reverses any legacy PR-#3 artifacts in the user's real ~/.codex
    (managed block in config.toml, sentinel auth.json, backup files).
    Does not launch Codex.

.PARAMETER DryRun
    Print what would be done (port choice, isolated CODEX_HOME path)
    without starting the bridge, seeding the isolated dir, or
    launching Codex.

.PARAMETER BridgePort
    Preferred bridge port. The launcher will search nearby ports if this
    one is busy. Default 20333.

.PARAMETER ProviderJson
    Path to omniroute-provider.json. Defaults to ./omniroute-provider.json
    relative to the script. May also be configured via env
    OMNIROUTE_PROVIDER_JSON, OMNIROUTE_BASE_URL+OMNIROUTE_API_KEY, or via
    ~/.config/opencode/auth.json (see codex-openai-omniroute-bridge.mjs).

.PARAMETER OpenProject
    Optional path to a project. If set, Codex is launched with
    `--open-project <path>`. Default: do not pre-open a project (Codex
    shows its normal project picker over the user's real filesystem).

.PARAMETER NoFreeformApplyPatch
    Do not emit `apply_patch_freeform = true` in the [features] sections of the
    isolated config.toml. Default: emit it as an additional safety net;
    literal apply_patch is handled by the local rewriter/helper path.

.PARAMETER UserCodexHomeRestoreTimeoutSec
    Maximum number of seconds the hidden restore watcher may leave the
    user-scope CODEX_HOME pointing at the isolated home. Default: 0,
    which means "until the activated Codex Desktop/AppX process tree has
    exited"; non-zero values are a safety ceiling for troubleshooting.

.NOTES
    - Never modifies the Microsoft Store Codex package.
    - Never writes OmniRoute managed blocks into %USERPROFILE%\.codex; a
      corrupted real config is backed up before deterministic repair.
    - The isolated CODEX_HOME is reseeded in place on every launch:
      auth.json + models_cache.json are refreshed from the user's real
      profile, managed config/tooling files are rewritten, and Codex
      history/state files are preserved.
    - On -Restore, the isolated CODEX_HOME is removed and any legacy
      PR-#3 artifacts in the user's real ~/.codex are reversed. Any
      stale user-scope CODEX_HOME or taskkill-shim PATH prefix left by an
      interrupted OmniRoute launch is also cleared if it points at this
      isolated home.
#>

