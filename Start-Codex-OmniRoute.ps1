function Get-ImportableConfigBlocks {
    param(
        [Parameter(Mandatory = $true)][string]$ConfigPath,
        [Parameter(Mandatory = $true)][string]$SourceLabel
    )

    $blocks = New-Object System.Collections.Generic.List[object]
    if (-not (Test-Path -LiteralPath $ConfigPath)) { return $blocks }

    $lines = [System.IO.File]::ReadAllLines($ConfigPath)
    $currentName = $null
    $currentLines = New-Object System.Collections.Generic.List[string]

    function Add-ConfigBlock {
        param(
            [AllowNull()][AllowEmptyString()][string]$Name,
            [Parameter(Mandatory = $true)]$Lines
        )
        if ([string]::IsNullOrWhiteSpace($Name)) { return }
        if (-not (Test-ImportableConfigSection -SectionName $Name)) { return }
        $blocks.Add([pscustomobject]@{
            Name = $Name
            Key = $Name.Trim().ToLowerInvariant()
            Text = (($Lines.ToArray()) -join "`r`n").Trim()
            Source = $SourceLabel
        })
    }

    foreach ($line in $lines) {
        $match = [regex]::Match($line, '^\s*\[(.+)\]\s*(?:#.*)?$')
        if ($match.Success) {
            Add-ConfigBlock -Name $currentName -Lines $currentLines
            $currentName = $match.Groups[1].Value.Trim()
            $currentLines = New-Object System.Collections.Generic.List[string]
            $currentLines.Add($line)
            continue
        }
        if ($null -ne $currentName) {
            $currentLines.Add($line)
        }
    }
    Add-ConfigBlock -Name $currentName -Lines $currentLines
    return $blocks
}

function Merge-ConfigBlocks {
    param([Parameter(Mandatory = $true)][AllowNull()][object[]]$Sources)

    $merged = New-Object System.Collections.Generic.List[object]
    foreach ($sourceBlocks in $Sources) {
        if ($null -eq $sourceBlocks) { continue }
        foreach ($block in @($sourceBlocks)) {
            $existingIndex = -1
            for ($i = 0; $i -lt $merged.Count; $i++) {
                if ([string]::Equals($merged[$i].Key, $block.Key, [System.StringComparison]::OrdinalIgnoreCase)) {
                    $existingIndex = $i
                    break
                }
            }
            if ($existingIndex -ge 0) {
                $merged.RemoveAt($existingIndex)
            }
            $merged.Add($block)
        }
    }
    return $merged
}

function ConvertTo-TomlString {
    param([AllowNull()][string]$Value)
    if ($null -eq $Value) { $Value = '' }
    $escaped = $Value.Replace('\', '\\').Replace('"', '\"').Replace("`r", '\r').Replace("`n", '\n').Replace("`t", '\t')
    return '"' + $escaped + '"'
}

function ConvertFrom-TomlBasicString {
    param([Parameter(Mandatory = $true)][string]$Value)
    $out = New-Object System.Text.StringBuilder
    for ($i = 0; $i -lt $Value.Length; $i++) {
        $c = $Value[$i]
        if ($c -eq '\' -and $i + 1 -lt $Value.Length) {
            $n = $Value[$i + 1]
            switch ($n) {
                '\' { [void]$out.Append('\'); $i++; continue }
                '"' { [void]$out.Append('"'); $i++; continue }
                'n' { [void]$out.Append("`n"); $i++; continue }
                'r' { [void]$out.Append("`r"); $i++; continue }
                't' { [void]$out.Append("`t"); $i++; continue }
            }
        }
        [void]$out.Append($c)
    }
    return $out.ToString()
}

function Repair-UserProfilePathText {
    param([Parameter(Mandatory = $true)][string]$Text)

    $userProfile = $env:USERPROFILE
    if ([string]::IsNullOrWhiteSpace($userProfile)) { return $Text }

    $singleUser = $userProfile
    $doubleUser = $userProfile.Replace('\', '\\')
    $repaired = $Text

    # Double-escaped TOML strings, e.g. "C:\\Users\\<mojibake>\\AppData\\..."
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]{2}Users[\\]{2}[^"''\r\n]{1,}?[\\]{2}AppData[\\]{2}Roaming[\\]{2}npm',
        ($doubleUser + '\\AppData\\Roaming\\npm'))
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]{2}Users[\\]{2}[^"''\r\n]{1,}?[\\]{2}\.cache[\\]{2}codex-runtimes',
        ($doubleUser + '\\.cache\\codex-runtimes'))
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]{2}Users[\\]{2}[^"''\r\n]{1,}?[\\]{2}\.codex[\\]{2}\.tmp',
        ($doubleUser + '\\.codex\\.tmp'))

    # Single-backslash literal strings and table names.
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]Users[\\][^"''\r\n]{1,}?[\\]AppData[\\]Roaming[\\]npm',
        ($singleUser + '\AppData\Roaming\npm'))
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]Users[\\][^"''\r\n]{1,}?[\\]\.cache[\\]codex-runtimes',
        ($singleUser + '\.cache\codex-runtimes'))
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]Users[\\][^"''\r\n]{1,}?[\\]\.codex[\\]\.tmp',
        ($singleUser + '\.codex\.tmp'))
    $repaired = [regex]::Replace(
        $repaired,
        'C:[\\]Users[\\][^"''\r\n]{1,}?[\\]documents[\\]codex',
        ($singleUser + '\documents\codex'))

    return $repaired
}

function Add-McpStdioShieldIfNeeded {
    param(
        [Parameter(Mandatory = $true)][string]$BlockText,
        [Parameter(Mandatory = $true)][string]$ScriptRoot,
        [Parameter(Mandatory = $true)][string]$NodeExe
    )

    if ($BlockText -match '^\s*url\s*=') { return $BlockText }
    if ($BlockText -match 'mcp-stdio-shield\.mjs') { return $BlockText }

    $cmdMatch = [regex]::Match($BlockText, '(?m)^command\s*=\s*"([^"]+)"\s*$')
    if (-not $cmdMatch.Success) { return $BlockText }

    $rawCommand = $cmdMatch.Groups[1].Value
    $command = ConvertFrom-TomlBasicString -Value $rawCommand
    $isNoisyWrapper = $false
    if ($command -match '(?i)(^|\\)powershell(\.exe)?$') { $isNoisyWrapper = $true }
    if ($command -match '(?i)\.(cmd|bat)$') { $isNoisyWrapper = $true }
    if (-not $isNoisyWrapper) { return $BlockText }

    $shield = Join-Path $ScriptRoot 'tools\mcp-stdio-shield.mjs'
    if (-not (Test-Path -LiteralPath $shield)) { return $BlockText }

    $argsMatch = [regex]::Match($BlockText, '(?m)^args\s*=\s*\[(.*)\]\s*$')
    $innerArgs = ''
    if ($argsMatch.Success) { $innerArgs = $argsMatch.Groups[1].Value.Trim() }

    $newCommandLine = 'command = ' + (ConvertTo-TomlString -Value $NodeExe)
    $newArgsInner = (ConvertTo-TomlString -Value $shield) + ', ' + (ConvertTo-TomlString -Value $command)
    if (-not [string]::IsNullOrWhiteSpace($innerArgs)) {
        $newArgsInner += ', ' + $innerArgs
    }
    $newArgsLine = 'args = [' + $newArgsInner + ']'

    $rewritten = [regex]::Replace($BlockText, '(?m)^command\s*=\s*"[^"]+"\s*$', $newCommandLine, 1)
    if ($argsMatch.Success) {
        $rewritten = [regex]::Replace($rewritten, '(?m)^args\s*=\s*\[.*\]\s*$', $newArgsLine, 1)
    } else {
        $rewritten = [regex]::Replace($rewritten, '(?m)^command\s*=.*$', ($newCommandLine + "`r`n" + $newArgsLine), 1)
    }
    return $rewritten
}

function Get-TopLevelTomlKeys {
    param([Parameter(Mandatory = $true)][string]$BlockText)

    $keys = @{}
    foreach ($line in @($BlockText -split "\r?\n")) {
        if ($line -match '^\s*\[') { continue }
        $match = [regex]::Match($line, '^\s*([A-Za-z0-9_-]+)\s*=')
        if ($match.Success) {
            $keys[$match.Groups[1].Value.ToLowerInvariant()] = $true
        }
    }
    return $keys
}

function Remove-ConflictingInlineChildBlocks {
    param([Parameter(Mandatory = $true)][object[]]$Blocks)

    $inlineKeysByParent = @{}
    foreach ($block in @($Blocks)) {
        $keys = Get-TopLevelTomlKeys -BlockText ([string]$block.Text)
        if ($keys.Count -gt 0) {
            $inlineKeysByParent[[string]$block.Key] = $keys
        }
    }

    $filtered = New-Object System.Collections.Generic.List[object]
    foreach ($block in @($Blocks)) {
        $key = [string]$block.Key
        $lastDot = $key.LastIndexOf('.')
        if ($lastDot -gt 0) {
            $parentKey = $key.Substring(0, $lastDot)
            $childKey = $key.Substring($lastDot + 1).ToLowerInvariant()
            if ($inlineKeysByParent.ContainsKey($parentKey) -and $inlineKeysByParent[$parentKey].ContainsKey($childKey)) {
                Write-Warning ("[omniroute] dropping imported [{0}] from {1}; parent [{2}] already defines inline key '{3}'" -f $block.Name, $block.Source, $parentKey, $childKey)
                continue
            }
        }
        $filtered.Add($block)
    }
    return $filtered
}

function Normalize-ImportedConfigBlocks {
    param(
        [Parameter(Mandatory = $true)][object[]]$Blocks,
        [Parameter(Mandatory = $true)][string]$ScriptRoot,
        [Parameter(Mandatory = $true)][string]$NodeExe
    )

    $normalized = New-Object System.Collections.Generic.List[object]
    foreach ($block in @($Blocks)) {
        $text = Repair-UserProfilePathText -Text ([string]$block.Text)
        if ($text.Length -gt 8000 -or ([string]$block.Name).Length -gt 500) {
            $displayName = [string]$block.Name
            if ($displayName.Length -gt 120) { $displayName = $displayName.Substring(0, 120) + '...' }
            Write-Warning ("[omniroute] dropping oversized imported config section [{0}] from {1}; it still looks corrupted after repair" -f $displayName, $block.Source)
            continue
        }
        $key = $block.Key
        if ($key.StartsWith('mcp_servers.', [System.StringComparison]::OrdinalIgnoreCase) -and
            -not $key.EndsWith('.env', [System.StringComparison]::OrdinalIgnoreCase)) {
            $text = Add-McpStdioShieldIfNeeded -BlockText $text -ScriptRoot $ScriptRoot -NodeExe $NodeExe
        }
        $normalized.Add([pscustomobject]@{
            Name = $block.Name
            Key = $block.Key
            Text = $text.Trim()
            Source = $block.Source
        })
    }
    return (Remove-ConflictingInlineChildBlocks -Blocks $normalized.ToArray())
}

