function Join-PathPrefix {
    param(
        [AllowNull()][string]$PathValue,
        [Parameter(Mandatory = $true)][string]$Prefix
    )

    if ([string]::IsNullOrWhiteSpace($PathValue)) { return $Prefix }
    $parts = @($PathValue -split ';' | Where-Object { -not [string]::IsNullOrWhiteSpace($_) })
    $filtered = @($parts | Where-Object { -not (Test-SameEnvironmentValue $_ $Prefix) })
    return (@($Prefix) + $filtered) -join ';'
}

function Clear-StaleTaskkillShimPathOverride {
    param([Parameter(Mandatory = $true)][string]$ShimDir)
    if (-not (Test-WindowsHost)) { return }
    if ([string]::IsNullOrWhiteSpace($ShimDir)) { return }

    $currentUserPath = [System.Environment]::GetEnvironmentVariable('PATH', 'User')
    if ([string]::IsNullOrWhiteSpace($currentUserPath)) { return }
    $parts = @($currentUserPath -split ';')
    if ($parts.Count -eq 0) { return }
    if (-not (Test-SameEnvironmentValue $parts[0] $ShimDir)) { return }
    $rest = @($parts | Select-Object -Skip 1)
    $newPath = ($rest -join ';')
    if ([string]::IsNullOrWhiteSpace($newPath)) { $newPath = $null }
    Set-UserPathOverride -Value $newPath
    Write-Host "[omniroute] cleared stale user-scope PATH taskkill shim prefix"
}

function Clear-StaleUserCodexHomeOverride {
    param([Parameter(Mandatory = $true)][string]$IsolatedHome)
    if (-not (Test-WindowsHost)) { return }

    $currentUserCodexHome = [System.Environment]::GetEnvironmentVariable('CODEX_HOME', 'User')
    if (Test-SameEnvironmentValue $currentUserCodexHome $IsolatedHome) {
        Set-UserCodexHomeOverride -Value $null
        Write-Host "[omniroute] cleared stale user-scope CODEX_HOME override"
    }
}

function Resolve-CSharpCompiler {
    $candidates = @()
    if ($env:WINDIR) {
        $candidates += (Join-Path $env:WINDIR 'Microsoft.NET\Framework64\v4.0.30319\csc.exe')
        $candidates += (Join-Path $env:WINDIR 'Microsoft.NET\Framework\v4.0.30319\csc.exe')
    }
    $cmd = Get-Command csc.exe -ErrorAction SilentlyContinue
    if ($cmd) { $candidates += $cmd.Source }
    foreach ($candidate in $candidates) {
        if ($candidate -and (Test-Path -LiteralPath $candidate)) { return $candidate }
    }
    return $null
}

function Ensure-TaskkillQuietShim {
    param(
        [Parameter(Mandatory = $true)][string]$IsolatedHome,
        [Parameter(Mandatory = $true)][string]$ScriptRoot
    )

    if (-not (Test-WindowsHost)) { return $null }
    $source = Join-Path $ScriptRoot 'tools\taskkill-quiet-shim.cs'
    if (-not (Test-Path -LiteralPath $source)) {
        Write-Warning "[omniroute] taskkill quiet shim source missing at $source"
        return $null
    }

    $shimDir = Join-Path $IsolatedHome 'bin'
    $shimExe = Join-Path $shimDir 'taskkill.exe'
    New-Item -ItemType Directory -Path $shimDir -Force | Out-Null

    $needsCompile = -not (Test-Path -LiteralPath $shimExe)
    if (-not $needsCompile) {
        $srcItem = Get-Item -LiteralPath $source -ErrorAction SilentlyContinue
        $exeItem = Get-Item -LiteralPath $shimExe -ErrorAction SilentlyContinue
        if ($srcItem -and $exeItem -and $exeItem.LastWriteTimeUtc -lt $srcItem.LastWriteTimeUtc) {
            $needsCompile = $true
        }
    }
    if (-not $needsCompile) { return $shimDir }

    $csc = Resolve-CSharpCompiler
    if (-not $csc) {
        Write-Warning "[omniroute] csc.exe not found; cannot build taskkill quiet shim. MCP app-server stdio may still be polluted by taskkill output."
        return $null
    }

    $raw = & $csc /nologo /target:exe /out:$shimExe $source 2>&1
    $exit = $LASTEXITCODE
    if ($exit -ne 0 -or -not (Test-Path -LiteralPath $shimExe)) {
        $detail = (($raw | Out-String).Trim())
        Write-Warning "[omniroute] failed to build taskkill quiet shim (exit=$exit): $detail"
        return $null
    }
    Write-Host "[omniroute] built taskkill quiet shim at $shimExe"
    return $shimDir
}

function ConvertTo-PowerShellLiteral {
    param([AllowNull()][string]$Value)
    if ($null -eq $Value) { return '$null' }
    return "'" + $Value.Replace("'", "''") + "'"
}

