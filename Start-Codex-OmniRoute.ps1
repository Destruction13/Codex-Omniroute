        [AllowNull()][string]$TaskkillShimDir,
        [AllowNull()][string]$PreviousUserPath,
        [AllowNull()][string]$ExpectedUserPath
    )
    if (-not (Test-WindowsHost)) { return }
    $currentUserCodexHome = [System.Environment]::GetEnvironmentVariable('CODEX_HOME', 'User')
    if (Test-SameEnvironmentValue $currentUserCodexHome $IsolatedHome) {
        Set-UserCodexHomeOverride -Value $PreviousUserCodexHome
        if ($null -eq $PreviousUserCodexHome) {
            Write-Host "[omniroute] restored user-scope CODEX_HOME (cleared temporary override)"
        } else {
            Write-Host "[omniroute] restored previous user-scope CODEX_HOME"
        }
    } else {
        Write-Warning "[omniroute] user-scope CODEX_HOME changed during activation; leaving current value untouched."
    }

    if (-not [string]::IsNullOrWhiteSpace($TaskkillShimDir)) {
        $currentUserPath = [System.Environment]::GetEnvironmentVariable('PATH', 'User')
        if ([string]::Equals($currentUserPath, $ExpectedUserPath, [System.StringComparison]::OrdinalIgnoreCase)) {
            Set-UserPathOverride -Value $PreviousUserPath
            Write-Host "[omniroute] restored user-scope PATH taskkill shim prefix"
        } elseif (-not [string]::IsNullOrWhiteSpace($currentUserPath)) {
            $parts = @($currentUserPath -split ';')
            if ($parts.Count -gt 0 -and (Test-SameEnvironmentValue $parts[0] $TaskkillShimDir)) {
                $rest = @($parts | Select-Object -Skip 1)
                $newPath = ($rest -join ';')
                if ([string]::IsNullOrWhiteSpace($newPath)) { $newPath = $null }
                Set-UserPathOverride -Value $newPath
                Write-Host "[omniroute] removed user-scope PATH taskkill shim prefix"
            } else {
                Write-Warning "[omniroute] user-scope PATH changed during activation; leaving current value untouched."
            }
        } else {
            Write-Warning "[omniroute] user-scope PATH changed during activation; leaving current value untouched."
        }
    }
}

