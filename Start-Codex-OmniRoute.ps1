function Restore-UserCodexHome {
    `$current = [System.Environment]::GetEnvironmentVariable('CODEX_HOME', 'User')
    if (-not (Test-SameEnvironmentValue `$current `$isolatedHome)) { return }
    if (`$hadPreviousUserCodexHome) {
        [System.Environment]::SetEnvironmentVariable('CODEX_HOME', `$previousUserCodexHome, 'User')
    } else {
        [System.Environment]::SetEnvironmentVariable('CODEX_HOME', `$null, 'User')
    }
    Publish-UserEnvironmentChange
}

function Restore-UserPath {
    if ([string]::IsNullOrWhiteSpace(`$taskkillShimDir)) { return }
    `$current = [System.Environment]::GetEnvironmentVariable('PATH', 'User')
    if ([string]::Equals(`$current, `$expectedUserPath, [System.StringComparison]::OrdinalIgnoreCase)) {
        if (`$hadPreviousUserPath) {
            [System.Environment]::SetEnvironmentVariable('PATH', `$previousUserPath, 'User')
        } else {
            [System.Environment]::SetEnvironmentVariable('PATH', `$null, 'User')
        }
        Publish-UserEnvironmentChange
        return
    }
    if ([string]::IsNullOrWhiteSpace(`$current)) { return }
    `$parts = @(`$current -split ';')
    if (`$parts.Count -eq 0) { return }
    if (-not (Test-SameEnvironmentValue `$parts[0] `$taskkillShimDir)) { return }
    `$rest = @(`$parts | Select-Object -Skip 1)
    `$newPath = (`$rest -join ';')
    if ([string]::IsNullOrWhiteSpace(`$newPath)) { `$newPath = `$null }
    [System.Environment]::SetEnvironmentVariable('PATH', `$newPath, 'User')
    Publish-UserEnvironmentChange
}

function Convert-ProcessCreationTimeUtc {
    param([AllowNull()]`$Value)
    if (`$null -eq `$Value) { return [datetime]::MinValue }
    try {
        if (`$Value -is [datetime]) { return `$Value.ToUniversalTime() }
        return ([System.Management.ManagementDateTimeConverter]::ToDateTime([string]`$Value)).ToUniversalTime()
    } catch {
        return [datetime]::MinValue
    }
}

function Get-CodexProcessRows {
    `$rows = @()
    try { `$rows = @(Get-CimInstance Win32_Process -ErrorAction SilentlyContinue) } catch { }
    if (`$rows.Count -eq 0) {
        try { `$rows = @(Get-WmiObject Win32_Process -ErrorAction SilentlyContinue) } catch { }
    }
    if (`$rows.Count -gt 0) { return `$rows }

    `$fallback = New-Object System.Collections.Generic.List[object]
    foreach (`$proc in @(Get-Process -ErrorAction SilentlyContinue)) {
        `$path = `$null
        `$start = [datetime]::MinValue
        try { `$path = `$proc.Path } catch { }
        try { `$start = `$proc.StartTime.ToUniversalTime() } catch { }
        `$fallback.Add([pscustomobject]@{
            ProcessId       = `$proc.Id
            ParentProcessId = 0
            Name            = (`$proc.ProcessName + '.exe')
            ExecutablePath  = `$path
            CreationDate    = `$start
        })
    }
    return @(`$fallback.ToArray())
}

function Get-TrackedCodexProcessCount {
    `$threshold = `$activationStartedAtUtc.AddSeconds(-10)
    `$rows = @(Get-CodexProcessRows)
    if (`$rows.Count -eq 0) { return 0 }

    `$tracked = New-Object 'System.Collections.Generic.HashSet[int]'
    foreach (`$row in `$rows) {
        `$pid = 0
        try { `$pid = [int]`$row.ProcessId } catch { continue }
        `$name = [string]`$row.Name
        `$path = [string]`$row.ExecutablePath
        `$createdUtc = Convert-ProcessCreationTimeUtc `$row.CreationDate
        `$isActivated = (`$activatedPid -gt 0 -and `$pid -eq [int]`$activatedPid)
        `$isPackageProcess = Test-PathUnderRoot -Path `$path -Root `$packageRoot
        `$isCodexNamed = (`$name -match '(?i)^codex.*\.exe\z')
        if (`$isActivated -or `$isPackageProcess -or (`$isCodexNamed -and `$createdUtc -ge `$threshold)) {
            [void]`$tracked.Add(`$pid)
        }
    }

    `$changed = `$true
    while (`$changed) {
        `$changed = `$false
        foreach (`$row in `$rows) {
            `$pid = 0
            `$ppid = 0
            try { `$pid = [int]`$row.ProcessId; `$ppid = [int]`$row.ParentProcessId } catch { continue }
            if (`$tracked.Contains(`$pid)) { continue }
            if (`$tracked.Contains(`$ppid)) {
                [void]`$tracked.Add(`$pid)
                `$changed = `$true
            }
        }
    }

    return `$tracked.Count
}

function Restore-And-Exit {
    Restore-UserCodexHome
    Restore-UserPath
    exit 0
}

`$sawTrackedProcess = `$false
`$emptySinceUtc = `$null
`$bootstrapDeadline = `$activationStartedAtUtc.AddSeconds(`$bootstrapGraceSec)
`$hardDeadline = [datetime]::MaxValue
if (`$timeoutSec -gt 0) {
    `$hardDeadline = (Get-Date).ToUniversalTime().AddSeconds(`$timeoutSec)
}

while ((Get-Date).ToUniversalTime() -lt `$hardDeadline) {
    `$current = [System.Environment]::GetEnvironmentVariable('CODEX_HOME', 'User')
    if (-not (Test-SameEnvironmentValue `$current `$isolatedHome)) {
        Restore-UserPath
        exit 0
    }

    `$trackedCount = Get-TrackedCodexProcessCount
    `$now = (Get-Date).ToUniversalTime()
    if (`$trackedCount -gt 0) {
        `$sawTrackedProcess = `$true
        `$emptySinceUtc = `$null
    } elseif (-not `$sawTrackedProcess) {
        if (`$now -ge `$bootstrapDeadline) { Restore-And-Exit }
    } else {
        if (`$null -eq `$emptySinceUtc) { `$emptySinceUtc = `$now }
        if (`$now -ge `$emptySinceUtc.AddSeconds(`$shutdownQuietSec)) { Restore-And-Exit }
    }

    Start-Sleep -Seconds 2
}
Restore-And-Exit
"@

    try {
        $encoded = [Convert]::ToBase64String([System.Text.Encoding]::Unicode.GetBytes($watcher))
        $cmd = Get-Command pwsh -ErrorAction SilentlyContinue
        if (-not $cmd) { $cmd = Get-Command powershell -ErrorAction SilentlyContinue }
        if (-not $cmd) { return $false }
        $args = @('-NoLogo', '-NoProfile', '-ExecutionPolicy', 'Bypass', '-EncodedCommand', $encoded)
        $startArgs = @{
            FilePath     = $cmd.Source
            ArgumentList = $args
            PassThru     = $true
        }
        if (Test-WindowsHost) {
            $startArgs['WindowStyle'] = 'Hidden'
        }
        $proc = Start-Process @startArgs
        if ($proc) {
            $leaseText = if ($TimeoutSec -gt 0) { "timeout=${TimeoutSec}s" } else { "lifecycle" }
            Write-Host ("[omniroute] user-scope CODEX_HOME/PATH lifecycle watcher running (pid={0}, {1})" -f $proc.Id, $leaseText)
            return $true
        }
    } catch { }
    return $false
}

